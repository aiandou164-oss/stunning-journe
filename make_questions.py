all_dicts = [
    ("strategy", "企業活動・法務・経営", strategy_terms),
    ("management", "システム開発・IT管理", management_terms),
    ("technology", "ネットワーク・DB・セキュリティ", technology_terms)
]

questions = []
qid = 100

for domain, cat, terms in all_dicts:
    keys = list(terms.keys())
    for key, correct_desc in terms.items():
        wrong_keys = random.sample([k for k in keys if k != key], 3)
        choices = {
            "ア": correct_desc,
            "イ": terms[wrong_keys[0]],
            "ウ": terms[wrong_keys[1]],
            "エ": terms[wrong_keys[2]]
        }
        
        choice_labels = ["ア", "イ", "ウ", "エ"]
        c_vals = list(choices.values())
        random.shuffle(c_vals)
        final_choices = {k: v for k, v in zip(choice_labels, c_vals)}
        ans = [k for k, v in final_choices.items() if v == correct_desc][0]
        
        explanation_lines = []
        for k, v in final_choices.items():
            matched_term = [term for term, desc in terms.items() if desc == v][0]
            prefix = "**【正解！】**" if k == ans else "【間違い】"
            explanation_lines.append(f"{k}．**{matched_term}**：\n{prefix} {v}")
                
        explanation_text = f"正解は「{ans}」の{key}だよ！\n\n" + "\n\n".join(explanation_lines)
        
        q = {
            "id": f"Q{qid}",
            "domain": domain,
            "category": cat,
            "difficulty": 1,
            "question": f"「{key}」の説明として、最も適切なものはどれか。",
            "choices": final_choices,
            "answer": ans,
            "explanation": explanation_text
        }
        questions.append(q)
        qid += 1

with open('data_questions_bulk.py', 'w', encoding='utf-8') as f:
    f.write("BULK_QUESTIONS = [\n")
    for q in questions:
        f.write("    " + repr(q) + ",\n")
    f.write("]\n")

print(f"Generated {len(questions)} questions.")
